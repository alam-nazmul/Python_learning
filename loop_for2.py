### Array to List
for item in ['alam', 'md', 'nazmul']:
    print(item)

### Range to List

for x in range(10):
    print(x)

for y in range(0, 10, 2):
    print(y)


### Sum of a array

prices = [10, 20, 30]
total = 0
for price in prices:
    total = total + price
print(f'The sum of array is {total}')
